#Splitting strings into arrays
puts "My firstname. My lastname. My Fullname.".split(/\./).inspect


#We split the output (string) based on . into an array
#and we converted into actual string using inspect
#p is nothing but puts along with inspect in it.


#Output of the program.
#My firstname
#My lastname
#My Fullname

#Use case :
#Lalitha asks for firstname and lastname
#Suresh asks for lastname
#Roshan asks for firstname
#Swaraj asks for fullname

#split is method which splits strings, ruby has already defined method.

#In ruby escape process is prefixed with backslash "\" , which gives more meaning to ruby
#if you do not do escape , it may interpret wrongly.
